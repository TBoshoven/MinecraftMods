package com.tomboshoven.minecraft.magicdoorknob.blocks;

import com.tomboshoven.minecraft.magicdoorknob.MagicDoorknobMod;
import mcp.MethodsReturnNonnullByDefault;
import net.minecraft.block.Block;
import net.minecraft.block.material.Material;
import net.minecraft.block.material.MaterialColor;
import net.minecraft.block.material.PushReaction;
import net.minecraftforge.event.RegistryEvent.Register;
import net.minecraftforge.eventbus.api.SubscribeEvent;
import net.minecraftforge.fml.common.Mod.EventBusSubscriber;

import javax.annotation.ParametersAreNonnullByDefault;

/**
 * Collection of all blocks in the mod.
 */
@EventBusSubscriber
@ParametersAreNonnullByDefault
@MethodsReturnNonnullByDefault
public final class Blocks {
    /**
     * The material the magic door and doorway are made of:
     * - Not liquid
     * - Solid
     * - Block movement
     * - Opaque
     * - Requires no tool
     * - Not flammable
     * - Not replaceable
     * - Block pushes
     */
    private static final Material DOORWAY_MATERIAL = new Material(MaterialColor.AIR, false, true, true, true, true, false, false, PushReaction.BLOCK);

    /**
     * A magic doorway block. Generated by a magic doorknob.
     */
    public static final Block MAGIC_DOORWAY = new MagicDoorwayBlock(Block.Properties.create(DOORWAY_MATERIAL))
            .setRegistryName(MagicDoorknobMod.MOD_ID, "magic_doorway");

    /**
     * A magic door block. Generated by a magic doorknob and the entrance to a magic doorway.
     */
    public static final Block MAGIC_DOOR = new MagicDoorBlock(Block.Properties.create(DOORWAY_MATERIAL))
            .setRegistryName(MagicDoorknobMod.MOD_ID, "magic_door");

    private Blocks() {
    }

    @SuppressWarnings("BoundedWildcard")
    @SubscribeEvent
    public static void registerBlocks(Register<Block> event) {
        event.getRegistry().registerAll(MAGIC_DOORWAY, MAGIC_DOOR);
    }
}
